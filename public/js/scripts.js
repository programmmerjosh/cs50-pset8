// Google Map
var map;

// markers for map
var markers = [];

// info window
var info = new google.maps.InfoWindow();

// execute when the DOM is fully loaded
$(function() {

    // styles for map
    // https://developers.google.com/maps/documentation/javascript/styling
    var styles = [

        // hide Google's labels
        {
            featureType: "all",
            elementType: "labels",
            stylers: [
                {visibility: "off"}
            ]
        },

        // hide roads
        {
            featureType: "road",
            elementType: "geometry",
            stylers: [
                {visibility: "off"}
            ]
        }

    ];

    // options for map
    // https://developers.google.com/maps/documentation/javascript/reference#MapOptions
    var options = {
        center: {lat: 42.3770, lng: -71.1256}, // Cambridge
        disableDefaultUI: true,
        mapTypeId: google.maps.MapTypeId.ROADMAP,
        maxZoom: 14,
        panControl: true,
        styles: styles,
        zoom: 13,
        zoomControl: true
    };

    // get DOM node in which map will be instantiated
    var canvas = $("#map-canvas").get(0);

    // instantiate map
    map = new google.maps.Map(canvas, options);

    // configure UI once Google Map is idle (i.e., loaded)
    google.maps.event.addListenerOnce(map, "idle", configure);

});

/**
 * Adds marker for place to map.
 */
function addMarker(place)
{
    // variables and marker specs
    var lat = place["latitude"];
    var lon = place["longitude"];
    var markerPosition = new google.maps.LatLng(lat, lon);
    var label = place["place_name"] + ", " + place["admin_name1"];
    var parameter = "geo="+ place["postal_code"];
    var content = ["<ul>"];
    var marker = new MarkerWithLabel({
        position: markerPosition,
        animation: google.maps.Animation.DROP,
        map: map,
        labelContent: label
    });
    
    // add a mouseover listener (animation)
    marker.addListener('mouseover', toggleBounce);
    
    // bounce animation for each marker
    function toggleBounce() {
        if (marker.getAnimation() !== null) {
          marker.setAnimation(null);
        } else {
          marker.setAnimation(google.maps.Animation.BOUNCE);
          setTimeout(function(){ marker.setAnimation(null); }, 750);
        }
    }

    // append markers to markers array
    markers.push(marker);

    // add a click listener
    google.maps.event.addListener(marker, "click", function (e){
    
    // clear the content    
    content = [];
    
    // set info window content to load image until data has been added to info window
    info.setContent("<img alt='loading' src='img/ajax-loader.gif'/>");
    
    // show info window
    info.open(map, marker);
        
    // insert appropriate data for info window
    $.getJSON("articles.php", parameter)
        .done(function(data) {
            $.each(data, function(i, item) {
                content.push("<li><a href='"+item.link+"' target='_blank'>"+item.title+"</a></li>");}); 
                content.push("</ul>");
                info.setContent(content.join("\n"));
        })
        .fail(function(jqXHR, textStatus, errorThrown) {
            // log error to browser's console
            console.log(errorThrown.toString());
        });
    });
}

